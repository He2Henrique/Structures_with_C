//explain typedef
// typedef is a keyword in C that allows us to create an alias for a data type.
// This can be useful for making code more readable and easier to maintain.
// For example, we can use typedef to renemae various data types like this:
typedef int my_int; // this creates an alias for the int data type

typedef unsigned int my_unsigned_int; // this creates an alias for the unsigned int data type
typedef long long my_long_long; // this creates an alias for the long long data type
typedef int* pointer; // this creates an alias for the pointer data type
// in this way...
// we can use my_int instead of int, my_unsigned_int instead of unsigned int, 
// and my_long_long instead of long long